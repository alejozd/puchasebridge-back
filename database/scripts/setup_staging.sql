SET SQL DIALECT 3;

/* XML_FILES table to store header information from validated XMLs */
CREATE TABLE XML_FILES (
    ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FILE_NAME VARCHAR(255) NOT NULL,
    PROVEEDOR_NOMBRE VARCHAR(255),
    FECHA_DOCUMENTO TIMESTAMP,
    ESTADO VARCHAR(20) DEFAULT 'VALIDADO',
    FECHA_CARGA TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT UK_XML_FILES_FILENAME UNIQUE (FILE_NAME)
);

/* XML_PRODUCTOS table to store line information from validated XMLs */
CREATE TABLE XML_PRODUCTOS (
    ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    XML_FILE_ID INTEGER NOT NULL,
    DESCRIPCION VARCHAR(500),
    REFERENCIA VARCHAR(100),
    CANTIDAD NUMERIC(18,4),
    UNIDADP VARCHAR(20),
    VALOR_UNITARIO NUMERIC(18,4),
    VALOR_TOTAL NUMERIC(18,4),
    EQUIVALENCIA_ID INTEGER,
    CONSTRAINT FK_XML_PRODUCTOS_FILE FOREIGN KEY (XML_FILE_ID) REFERENCES XML_FILES(ID) ON DELETE CASCADE,
    CONSTRAINT FK_XML_PRODUCTOS_EQUIV FOREIGN KEY (EQUIVALENCIA_ID) REFERENCES EQUIVALENCIA(ID)
);
