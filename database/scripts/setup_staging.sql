SET SQL DIALECT 3;

CREATE TABLE XML_FILES (
    ID                INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FILE_NAME         VARCHAR(255) CHARACTER SET ISO8859_1,
    RUTA              VARCHAR(500) CHARACTER SET ISO8859_1,
    PROVEEDOR_NIT     VARCHAR(20) CHARACTER SET ISO8859_1,
    PROVEEDOR_NOMBRE  VARCHAR(255) CHARACTER SET ISO8859_1,
    FECHA_DOCUMENTO   DATE,
    ESTADO            VARCHAR(20) CHARACTER SET ISO8859_1,
    MENSAJE_ERROR     VARCHAR(500) CHARACTER SET ISO8859_1,
    FECHA_CARGA       TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FECHA_VALIDACION  TIMESTAMP,
    FECHA_PROCESO     TIMESTAMP,
    CONSTRAINT UK_XML_FILES_FILENAME UNIQUE (FILE_NAME)
);

CREATE TABLE XML_PRODUCTOS (
    ID                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    XML_FILE_ID         INTEGER,
    DESCRIPCION         VARCHAR(255) CHARACTER SET ISO8859_1,
    REFERENCIA          VARCHAR(100) CHARACTER SET ISO8859_1,
    REFERENCIA_STD      VARCHAR(100) CHARACTER SET ISO8859_1,
    CANTIDAD            NUMERIC(18,6),
    VALOR_UNITARIO      NUMERIC(18,6),
    VALOR_TOTAL         NUMERIC(18,6),
    IMPUESTO            NUMERIC(18,6),
    ESTADO_VALIDACION   VARCHAR(20) CHARACTER SET ISO8859_1,
    MENSAJE_VALIDACION  VARCHAR(500) CHARACTER SET ISO8859_1,
    EQUIVALENCIA_ID     INTEGER,
    CONSTRAINT FK_XML_PRODUCTOS_FILE FOREIGN KEY (XML_FILE_ID) REFERENCES XML_FILES(ID) ON DELETE CASCADE,
    CONSTRAINT FK_XML_PRODUCTOS_EQUIV FOREIGN KEY (EQUIVALENCIA_ID) REFERENCES EQUIVALENCIA(ID)
);

CREATE INDEX IDX_XML_PRODUCTOS_FILE ON XML_PRODUCTOS (XML_FILE_ID);
